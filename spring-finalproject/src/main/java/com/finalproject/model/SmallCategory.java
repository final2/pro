package com.finalproject.model;

public class SmallCategory {

	private int no;
	private String name;
	private LargeCategory largeCat;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public LargeCategory getLargeCat() {
		return largeCat;
	}
	public void setLargeCat(LargeCategory largeCat) {
		this.largeCat = largeCat;
	}
	
	
}
